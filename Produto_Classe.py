class Produtos():
    def __init__(self,nome, preco, quant):
        self.__produto  = nome
        self.__valor = preco
        self.__quantidade = quant
        #Buscar informação 
        def get_produto(self):
            return self.__produto
        def get_valor(self):
            return self.__valor 
        def get_quantidade(self):
            return self.__quantidade
        
